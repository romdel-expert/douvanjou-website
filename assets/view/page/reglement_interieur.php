        <h2>REGLEMENT INTERIEUR</h2>

        <h4>Article 1- L’association</h4>

        <p style="margin: 20px 0; text-align: justify;">
            L’association est représentée par le président.
            Elle est composée d’un comité central siégeant en France et de représentants en
            Haïti et en Amérique nommés par le comité central, agissant par délégation.
        </p>

        <h4>Article 2- Agrément de nouveaux membres</h4>

        <p style="margin: 20px 0; text-align: justify;">
            Tout nouveau membre doit prendre connaissance et accepter les conditions du
            règlement intérieur de l’association.
            Il est agréé par le conseil statuant à la majorité de tous ses membres.
            Le conseil statue lors de chacune de ses réunions sur les demandes d’admission
            présentées.
        </p>

        <p  style="margin: 20px 0; text-align: justify;">
            Les personnes désirant adhérer doivent remplir un bulletin d’adhésion.
        </p>

        <h4>Atricle 3- Démission- exclusion- décès d’un membre</h4>

        <p style="margin: 20px 0; text-align: justify;">
            <ol>
                <li>
                    La démission doit être adressée au président du conseil par lettre
                    recommandée. Elle n’a pas à être motivée par le membre
                    démissionnaire.
                </li>
                <li>
                    L’exclusion d’un membre peut être prononcée par le conseil, pour
                    motif grave. Sont notamment réputés constituer des motifs
                    graves :

                    <ul>
                        <li>
                            la non-participation aux activités de l’association (plus de trois
                            réunions) ;
                        </li>
                        <li>
                            une condamnation pénale pour crime et délit ;
                        </li>
                        <li>
                            toute action de nature à porter préjudice, directement ou
                            indirectement, aux activités de  ’association ou à sa réputation.
                        </li>
                    </ul>

                    <p class="text-blue"  style="margin: 20px 0; text-align: justify;">
                        En tout état de cause, l’intéressé doit être mis en mesure de présenter
                        sa défense, préalablement à la décision d’exclusion dans un délai de
                        trente (30) jours pendant lequel l’intéressé est mis à pied.
                    </p>
                </li>
                <li>
                    En cas de décès d’un membre, les héritiers ou les légataires ne
                    peuvent prétendre à un quelconque maintien dans l’association.

                    <p style="margin: 20px 0; text-align: justify;">
                        La cotisation versée à l’association est définitivement acquise,
                        même cas en cas de démission, d’exclusion, ou de décès d’un
                        membre en cours d’année.
                    </p>
                </li>
            </ol>
        </p>

        <h4>
            Article 4- Assemblées générales - Modalités applicables aux
            votes
        </h4>

        <p>
        <ol>
            <li>
                Votes des membres présents Les membres présents votent à main
                levée. Toutefois, un scrutin secret peut être demandé par le conseil ou
                50%+ 1 des membres présents.
            </li>
            <li>
                Votes par procuration Comme indiqué à l’article « N » des statuts
                <p style="margin: 20px 0; text-align: justify;">
                    Si un membre de l’association ne peut assister personnellement à une
                    assemblée, il peut s’y faire représenter par un mandataire, membre de
                    l’association.
                </p>
            </li>
        </ol>
        </p>

        <h4>Article 5- Indemnités de remboursement.</h4>

        <p style="margin: 20px 0; text-align: justify;">
            Seuls les administrateurs chargés d’une mission <span class="text-blue">et/ou</span> membres élus du
            bureau, peuvent prétendre au remboursement des frais engagés dans
            le cadre de leurs fonctions et sur justifications.
        </p>

        <p style="margin: 20px 0; text-align: justify;">
            Il est possible, dans ce cas, d’abandonner ces remboursements et d’en
            faire don à l’association en vue de la reduction d’impot sur le revenue
        </p>

        <h4>Article 6- Commission de travail.</h4>

        <p style="margin: 20px 0; text-align: justify;">
            Des commissions de travail peuvent être constituées par décision du
            conseil d’administration.
        </p>

        <h4>Article 7- réunion</h4>

        <p style="margin: 20px 0; text-align: justify;">
            L’association reunira ses dirigeants regulierement.
        </p>

        <p style="margin: 20px 0; text-align: justify;">
            Les dirigeants, selon les besoins et en function de la realite du moment
            peuvent convoquer tous les membres de l’association. Toute decision
            et meme au sein du comite executive doit faire l’objet d’un vote et d’un
            procès verbal
        </p>

        <h4>Article 8– Modification du règlement intérieur</h4>

        <p style="margin: 20px 0; text-align: justify;">
            Le présent règlement intérieur pourra être modifié par le conseil ou par
            l’assemblée générale ordinaire à la majorité des deux tiers des
            membres.
        </p>
